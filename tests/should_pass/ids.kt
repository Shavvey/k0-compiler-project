fun ids() {
  // Simple IDs first
  var s: String = "Simple ID"
  // koltin allow the use of keywords via `` escapes
  var `++`: String = "Escaped ID"
  var `Int`: Int = "Another escape with `"
  var `it`: String = "Stuff"
  var `in`: Double = 0b1
  var a13a: Double = 1E3
  var ____13aa__: Int = 0xFF
  var ____String___: String = "aaaa\b\b\b\b"
  return 0
}
